"""Reports cocotb tests using the generated JUnit ``results.xml`` file."""

from __future__ import annotations

import glob
import re
from pathlib import Path
from typing import Any
from xml.etree import ElementTree as ET

import pytest
from pytest import CallInfo, Config, Item, TestReport, hookimpl

from cocotb_tools.pytest._collector import Simulation

FILE_LINE: re.Pattern[str] = re.compile(r'^\s*File\s+"(.*)",\s+line\s+([0-9]+).*$')
"""Capture file and line from raised exception traceback."""


class Testcase(Item):
    """A pytest test item representing a single cocotb testcase extracted from a JUnit XML report."""

    def __init__(self, element: ET.Element, **kwargs: Any) -> None:
        """Create a new Testcase item from an XML element.

        Args:
            element: The XML element containing testcase data.
            **kwargs: Additional keyword arguments passed to the parent Item.
        """
        properties: dict[str, Any] = get_properties(element)
        classname: str = element.attrib["classname"]
        name: str = element.attrib["name"]
        file: str = properties["file"]
        line: int = int(properties.get("line", 0))

        super().__init__(f"{classname}.{name}", path=Path(file), **kwargs)
        self.element: ET.Element = element
        self.user_properties.extend(properties.items())
        self.location = (file, line, name)

    def runtest(self) -> None:
        """Evaluate the testcase status from the JUnit XML element.

        Raises:
            pytest.skip.Exception: If the testcase was marked as skipped.
            Exception: If the testcase failed or encountered an error, matching the type reported in the XML.
        """
        exception: str | None = None
        message: str | None = None

        for section in ("system-out", "system-err", "error", "failure"):
            key: str = "stdout" if section == "system-out" else "stderr"

            for element in self.element.findall(section):
                if "type" in element.attrib:
                    exception = element.attrib["type"]
                    message = element.attrib.get("message")

                if element.text:
                    self.add_report_section("call", key, element.text)

        for element in self.element.findall("skipped"):
            pytest.skip(element.attrib.get("message", "skipping cocotb test"))

        if exception:
            raise RuntimeError(f"{exception}: {message}")


class ResultsXML:
    """A pytest plugin class that reads JUnit XML reports generated by the simulator runner and generates pytest reports."""

    def __init__(self, config: Config) -> None:
        """Create a new instance of the XML testcase reporter."""
        self.config: Config = config
        self._seen: set[Any] = set()

    @hookimpl(tryfirst=True)
    def pytest_runtest_makereport(
        self, item: Item, call: CallInfo[None]
    ) -> TestReport | None:
        """Create a pytest TestReport for a test item phase, parsing simulation XML results if applicable.

        Args:
            item: The pytest item.
            call: The :class:`~pytest.CallInfo` for the phase.

        Returns:
            The test report, or :data:`None` if skipped.
        """
        report: TestReport = TestReport.from_item_and_call(item, call)

        if call.when != "call":
            return report

        hook = self.config.hook
        start: float = call.start
        parent: Any
        results_xml: str

        if isinstance(item, Simulation):
            # Plugin is using an unique build directory with the DUT identifier (hashed build context)
            results_xml = f"{item.dut.id}/results.xml"
            parent = item.parent
        else:
            # Name of XML file generated by Runner is based on the PYTEST_CURRENT_TEST environment variable
            results_xml = glob.escape(f"{item.name}.result.xml")
            parent = item

        for xml in self.config.invocation_params.dir.rglob(results_xml):
            if xml not in self._seen:
                self._seen.add(xml)

                for testsuite in ET.parse(xml).getroot().findall("testsuite"):
                    for testcase in testsuite.findall("testcase"):
                        test = Testcase.from_parent(parent, element=testcase)
                        call = CallInfo.from_call(test.runtest, when="call")
                        duration = float(testcase.attrib.get("time", 0))

                        call.start = start
                        call.duration = duration
                        call.stop = start + duration
                        start = call.stop

                        test_report = TestReport.from_item_and_call(test, call)
                        update_report(test_report, call)
                        hook.pytest_runtest_logreport(report=test_report)

        return report


def update_report(report: TestReport, call: CallInfo) -> None:
    """Update the test report with information about raised error.

    Args:
        report: The test report to update.
        call: Information about test function call.
    """
    if report.passed:
        return

    # As default, use file and line of test function
    file: str = report.location[0]
    line: int = report.location[1] or 0
    message: str = str(call.excinfo.value) if call.excinfo else report.outcome

    for section in report.get_sections("Captured stderr call"):
        for text in section[1].splitlines():
            # Capture the last file and line from captured exception traceback in stderr
            if "File" in text and "line" in text:
                match: re.Match[str] | None = FILE_LINE.match(text)

                if match:
                    file = str(match.group(1))
                    line = int(match.group(2))

    # Assign captured information (file, line, message) about raised exception to test report
    report.longrepr = (file, line, message)


def get_properties(element: ET.Element) -> dict[str, Any]:
    """Extract property key-value pairs from a JUnit XML testcase element.

    Args:
        element: The XML element representing the testcase.

    Returns:
        A dictionary of testcase properties.
    """
    properties: dict[str, Any] = {}

    for items in element.findall("properties"):
        for item in items.findall("property"):
            name: str | None = item.attrib.get("name")

            if name:
                properties[name] = item.attrib.get("value")

    return properties
